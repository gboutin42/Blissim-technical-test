/* SQL QUERY */

/* Get firstname and lastname of the customers who are order the product with id 1 */
SELECT c.firstname as firstname, c.lastname as lastname
FROM orders as o
LEFT JOIN details_orders as do ON do.id_order=o.id_order
LEFT JOIN customers as c ON c.id_customer=o.id_customer
WHERE do.id_product = 1;

/* Get names and quantities order during the 7 last days */
SELECT do.id_product as id_product, p.name as name, SUM(do.quantity) as qty 
FROM orders as o 
LEFT JOIN details_orders as do ON do.id_order=o.id_order 
LEFT JOIN products as p ON p.id_product=do.id_product 
WHERE o.date_add BETWEEN DATE_SUB(NOW(), INTERVAL 7 DAY) AND NOW() 
GROUP BY id_product;
