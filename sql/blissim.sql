/* CREATE DATABASE */
CREATE DATABASE IF NOT EXISTS `blissim`;

/* CREATE TABLES */
CREATE TABLE IF NOT EXISTS `customers`
	(
		`id_customer`			INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`firstname`				VARCHAR(255) NOT NULL,
		`lastname`				VARCHAR(255) NOT NULL,
		`phone_number`			VARCHAR(50) NOT NULL,
		`id_address`			INT NOT NULL,
		`mail`					VARCHAR(255) NOT NULL,
		`password`				VARCHAR(255) NOT NULL,
		`validate_account`		BOOLEAN NOT NULL DEFAULT '0',
		`date_add`				DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
	);

CREATE TABLE IF NOT EXISTS `products`
	(
		`id_product`			INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`name`					VARCHAR(255) NOT NULL,
		`reference`         	VARCHAR(255) NOT NULL,
		`url_picture`   		VARCHAR(4000) NOT NULL,
		`description`       	TEXT COLLATE utf8mb4_general_ci NOT NULL,
		`stock`             	INT NOT NULL,
		`price_ht`          	DECIMAL(12,2) NOT NULL,
		`date_add`          	DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
		`date_update`       	DATETIME
	);

CREATE TABLE IF NOT EXISTS `orders`
	(
		`id_order`          	INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`id_customer`       	INT NOT NULL,
		`id_state`				INT NOT NULL,
		`total_price`			DECIMAL(12,2) NOT NULL,
		`date_add`          	DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
	);

CREATE TABLE IF NOT EXISTS `orders_state`
	(
		`id_state`				INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`state`					VARCHAR(255) NOT NULL
	);

CREATE TABLE IF NOT EXISTS `details_orders`
	(
		`id_detail_order`       INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`id_order`				INT NOT NULL,
		`id_delivery_address`	INT NOT NULL,
		`id_bild_address`		INT NOT NULL,
		`id_product`	       	INT NOT NULL,
		`quantity`				INT NOT NULL,
		`tva`					INT NOT NULL,
		`date_add`          	DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
	);

CREATE TABLE IF NOT EXISTS `addresses`
	(
		`id_address`			INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
		`number_street` 		INT NOT NULL,
		`street`				VARCHAR(1000),
		`city`					VARCHAR(255),
		`zip_code`				VARCHAR(50),
		`country`				VARCHAR(255)
	);

